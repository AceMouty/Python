def main():
    # ints: 4, 57, and -10
    # floating point: 6.1, 1.3333, 0.0
    print("Then number 10 is of type...")
    print(type(10))
    print("The number 11.23 is of type...")
    print(type(11.23))
    print()
    print("1 + 1 =", 1+1)
    print("5 - 3 =", 5 - 3)
    # single / will return a float
    print("18 / 2 =", 18 / 2)
    # double // will return a int (floored division)
    print("18 // 2 = ", 18 // 2)
    print("15 % 2 =", 15 % 2)
    # ** exponent
    print("2 ** 3 =", 2 ** 3)


if __name__ == "__main__":
    main()
