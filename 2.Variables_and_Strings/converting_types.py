def main():

    # with Python we are able to change the
    # data type of a value / variable
    # most if not ALL the data types have this
    # wrapper function / method for casting data from
    # one type to another.

    # convert a string to a number
    my_str = "8"
    print(1 + int(my_str))
    print()

    # convert a int to a str
    my_age = 23
    print("I am " + str(my_age) + " years old")


if __name__ == "__main__":
    main()
